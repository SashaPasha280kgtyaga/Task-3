def конвертировать_время(время):
    if "AM" in время or "PM" in время:
        часы, минуты, период = map(int, время[:-6].split(":"))
        
        if "PM" in время and часы != 12:
            часы += 12
        elif "AM" in время and часы == 12:
            часы = 0

        return "{:02d}:{:02d}".format(часы, минуты)

    else:
        часы, минуты = map(int, время.split(":"))
        период = "AM" if часы < 12 else "PM"

        if часы == 0:
            часы = 12
        elif часы > 12:
            часы -= 12

        return "{:02d}:{:02d} {}".format(часы, минуты, период)

время_12ч = "03:45 PM"
время_24ч = "18:30"

результат_12ч = конвертировать_время(время_12ч)
результат_24ч = конвертировать_время(время_24ч)

print(f"Исходное время (12-часовой формат): {время_12ч}")
print(f"Результат (24-часовой формат): {результат_12ч}\n")
